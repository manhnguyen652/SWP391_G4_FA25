package model;
public class Publisher {
    private int id;
    private String name;
    private String addr;

    // Constructors
    public Publisher() {
    }

    public Publisher(int id, String name, String addr) {
        this.id = id;
        this.name = name;
        this.addr = addr;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }
}